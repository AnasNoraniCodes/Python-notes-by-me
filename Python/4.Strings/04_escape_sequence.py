#Escape Sequence
#\n used to shift to new line
#\t used to give one tab space
#"My name is \"Anas"\ . " etc and so on...

print("I am Anas Norani.I am 17 years old.\n")
#for new line
print("I am Anas Norani.\nI am 17 years old.\n")
#for double quotes
print("I am \"Anas Norani\".\nI am 17 years old.\n")
#for one tap space
print("I am Anas \t Norani.\nI am 17 years old.\n")
 
