#Translation of words using dict...

#dict created
words_dict = {
    'madad' : 'Help',
    'kamra' : 'Room',
    'kuta'  : 'Dog'
}

#getting user word and stored in variable
user_word = input('Enter word that you want the meaning of :')

#printing word from dict
print(f"The meaning of {user_word}in English is ",words_dict[user_word])

